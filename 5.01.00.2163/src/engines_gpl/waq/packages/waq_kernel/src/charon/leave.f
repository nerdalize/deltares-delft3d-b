      subroutine leave(k, niet)
      implicit none
      integer k, niet
c        4-69
c        if toggle is zero, exit
c        if toggle is non-zero, increase value and return
      if (k.eq.0) go to 10
      k = k + 1
      return
   10 write (niet,99999)
      call chexit
99999 format (35h0*** subroutine leave goes to exit./1h )
      return
      end
